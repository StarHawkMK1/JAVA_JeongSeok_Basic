// 4. 연산자의 결합규칙

public class Ex03_01 {
    public static void main(String[] args) {
        int x, y;

        x = y = 3;          // y에 3이 저장된 후, x에 3이 저장.
        System.out.println("x = " + x);
        System.out.println("y = " + y);
    }
}
