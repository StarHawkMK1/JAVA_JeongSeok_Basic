package Java_JeongSeok_Basic.Ch3.Example;

// 10. 산술 변환에 대한 이해

public class Ex3_7 {
    public static void main(String[] args) {
        System.out.println(5 / 2);          // int타입 / int타입 = int타입
        System.out.println(5 / (float)2);   // 실수값를 연산 결과로 받기 위해서는 어느 한 쪽을 형변환 시켜줘야함.
    }                                       // 5를 형변환시켜도 결과는 동일.
}
