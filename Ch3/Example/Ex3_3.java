package Java_JeongSeok_Basic.Ch3.Example;

// 5. 증감 연산자의 활용2

public class Ex3_3 {
    public static void main(String[] args) {
        int i = 5, j = 5;

        System.out.println(i++);        // i의 값을 출력한 후, 1 증가시킴.
        System.out.println(++j);        // j의 값을 1 증가시킨 후, 출력.
        System.out.println("i = " + i + ", j = " + j);
    }
}
